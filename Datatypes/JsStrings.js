// Strings are written with quotes. You can use single or double quotes:
 
//using double quotes:
let firstName = "Sirisha";

//using single quotes:
let lastName = "Ardhala";
console.log("The result of the :" + firstName + lastName);


// You can use quotes inside a string, as long as they don't match the quotes surrounding the string:

//single quotes inside double quotes:
let answer1 = "It's alright";

//single quotes inside double quotes:
let answer2 = "My name is 'Sirisha' ";

//Double quotes inside single quotes:
let answer3 = 'I am from "guntur" ';
console.log("The result of the values :" +answer1+answer2+answer3);